//利用递归实现。在一组有 n 个元素的有序数据中折半查找关键字 key，如果找到，返回 key 所在的位置，否则返回-1。
//输入形式：第一行参数 N，M；N 为这一组数的个数，M 为待查找的数；第二行为 N 个整数，为排序后的数。
//输出形式：一个整数 J，若能找到，这该整数在数组内的下标，若无法找到，则输出-1；

#include<stdio.h>
int main()
{
    int n=0,key=0;
    scanf("%d",&n);
    int Array[n];
    scanf("%d",&key);
    for(int i=0;i<n;i++)
    {
        scanf("%d",&Array[i]);
    }
    int start=0;
    int end =n-1;
    int middle=0;
    while(start<=end)
    {
        middle=(start+end)/2;
        if(Array[middle]==key)
        {
            printf("%d",middle);
            break;
        }
        else if(Array[middle]>key)
        {
            end=middle-1;
        }
        else 
        start=middle+1;
        if(start>=end-1&&Array[start]!=key&&Array[end]!=key)
        {
            printf("-1");
            break;

        }
        
    }


}